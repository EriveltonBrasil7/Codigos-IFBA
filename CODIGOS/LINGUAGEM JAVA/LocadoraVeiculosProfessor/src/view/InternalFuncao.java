    private void btFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btFiltrarActionPerformed
        String campo = cbFiltrar.getSelectedItem().toString();
        if(campo.equals("Nome")){
            campo = "funca_nome";
        }else{
            campo = "funca_id";
        }

        String valor = tfFiltrar.getText();

        List<Funcao> listaTemporaria = dao.getFiltrarFuncoes(campo, valor, ordenacao);

        if(listaTemporaria.isEmpty()){
            JOptionPane.showMessageDialog(this, "Ops! não encontrei nada!");
        }else{
           listaFuncoes = listaTemporaria;
           preencherTabela();
        }
    }//GEN-LAST:event_btFiltrarActionPerformed

