    public void preencherTabela() {
        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setNumRows(0);
        Object[] linha;
        
        for(Funcao f : listaFuncoes){
            linha = new Object[]{f.getId(), f.getNome()};
            model.addRow(linha);
        }
        
        jTable1.setRowSelectionInterval(0, 0);
    }

