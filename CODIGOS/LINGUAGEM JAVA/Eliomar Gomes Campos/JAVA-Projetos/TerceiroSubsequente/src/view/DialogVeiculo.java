    private void bSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSalvarActionPerformed
        Veiculo vei = new Veiculo();
        vei.setAno(tfAno.getText());
        vei.setCor(tfCor.getText());
        vei.setMarca(tfMarca.getText());
        vei.setModelo(tfModelo.getText());
        vei.setNome(tfNome.getText());
        vei.setPlaca(tfPlaca.getText());

        if (tfCodigo.getText().isEmpty()) {
            dao.inserirVeiculo(vei);
            frameAnterior.setListaVeiculos(dao.getAllVeiculos(frameAnterior.getOrdena()));
            frameAnterior.preencherTabela();
            posicao = frameAnterior.getListaVeiculos().size() - 1;
            veiculo = frameAnterior.getListaVeiculos().get(posicao);
            JOptionPane.showMessageDialog(this, "Salvo com Sucesso");
        } else {
            vei.setId(Integer.parseInt(tfCodigo.getText()));
            dao.atualizarVeiculo(vei); 
            frameAnterior.setListaVeiculos(dao.getAllVeiculos(frameAnterior.getOrdena()));
            frameAnterior.preencherTabela();
            veiculo = frameAnterior.getListaVeiculos().get(posicao);
            JOptionPane.showMessageDialog(this, "Atualizado com Sucesso");
        }

        atualizarFormulario();

    }//GEN-LAST:event_bSalvarActionPerformed

    private void bPrimeiroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bPrimeiroActionPerformed
        posicao = 0;
        veiculo = frameAnterior.getListaVeiculos().get(posicao);
        frameAnterior.getTabela().setRowSelectionInterval(posicao, posicao);
        atualizarFormulario();
    }//GEN-LAST:event_bPrimeiroActionPerformed

    private void bUltimoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUltimoActionPerformed
        posicao = frameAnterior.getListaVeiculos().size() - 1;
        veiculo = frameAnterior.getListaVeiculos().get(posicao);
        frameAnterior.getTabela().setRowSelectionInterval(posicao, posicao);
        atualizarFormulario();
    }//GEN-LAST:event_bUltimoActionPerformed

    private void bAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAnteriorActionPerformed
        posicao--;
        if (posicao < 0) {
            JOptionPane.showMessageDialog(this, "Já está no Primeiro Registro!");
            posicao++;
        } else {
            veiculo = frameAnterior.getListaVeiculos().get(posicao);
            frameAnterior.getTabela().setRowSelectionInterval(posicao, posicao);
            atualizarFormulario();
        }


    }//GEN-LAST:event_bAnteriorActionPerformed

    private void bProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bProximoActionPerformed
        posicao++;
        if (posicao > frameAnterior.getListaVeiculos().size() - 1) {
            JOptionPane.showMessageDialog(this, "Já está no Derradero Registro!");
            posicao--;
        } else {
            veiculo = frameAnterior.getListaVeiculos().get(posicao);
            frameAnterior.getTabela().setRowSelectionInterval(posicao, posicao);
            atualizarFormulario();
        }
    }//GEN-LAST:event_bProximoActionPerformed

    private void bEscluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bEscluirActionPerformed

        int opcao = JOptionPane.showConfirmDialog(this, "Tem certeza?","Alerta!",JOptionPane.YES_NO_OPTION);
        if (opcao == 0) { //0 é sim, 1 é não e 2 é cancelar
            dao.deletarVeiculo(veiculo);
            frameAnterior.setListaVeiculos(dao.getAllVeiculos(frameAnterior.getOrdena()));
            frameAnterior.preencherTabela();
            if (posicao > 0) {
                posicao--;
            }
            veiculo = frameAnterior.getListaVeiculos().get(posicao);
            atualizarFormulario();
            JOptionPane.showMessageDialog(this, "Excluído com sucesso!");
        }

    }//GEN-LAST:event_bEscluirActionPerformed

