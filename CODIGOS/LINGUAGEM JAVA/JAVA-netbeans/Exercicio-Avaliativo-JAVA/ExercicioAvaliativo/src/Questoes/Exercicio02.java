    private void jbtnCliqueAquiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnCliqueAquiActionPerformed
        int anoAtual, anoNascimento, idade;
        
        anoNascimento = Integer.parseInt(jtfAnoNascimento.getText());
        anoAtual = Integer.parseInt(jtfAnoAtual.getText());
        
        idade = anoAtual - anoNascimento;
        
        if(idade>=18 && idade<=70){
            jlResultado.setText("Sua idade é "+idade+" anos, Seu voto é obrigatório!");
        }else if(idade>=16 && idade >70){
            jlResultado.setText("Sua idade é "+idade+" anos, Seu voto é facultativo!");
        }else if(idade <16){
            jlResultado.setText("Sua idade é "+idade+" anos, Seu voto é proibido!");
        }
        
    }//GEN-LAST:event_jbtnCliqueAquiActionPerformed

