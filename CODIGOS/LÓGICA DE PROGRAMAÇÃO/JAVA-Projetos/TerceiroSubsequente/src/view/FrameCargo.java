    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        Cargo c = new Cargo();
        c.setNome(textoNome.getText());
        c.setDescricao(textoDescricao.getText());
        c.setRemuneracao(Double.parseDouble(textoRemuneracao.getText()));

        boolean status = dao.insertCargo(c);

        if (status == true) {
            JOptionPane.showMessageDialog(this, "Salvo com sucesso!");
            int opcao = JOptionPane.showConfirmDialog(this, "Deseja salvar outro cargo?", "Mensagem", JOptionPane.YES_NO_OPTION);

            if (opcao == 0) {
                limparFrame();
            } else {
                lista = dao.getAllCargos(ordenacao);
                posicao = lista.size() - 1;
                preencherFrame();
                preencherTabela();
                botaoSalvar.setEnabled(false);
                botaoAtualizar.setEnabled(true);
                botaoExcluir.setEnabled(true);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar!");
        }


    }//GEN-LAST:event_botaoSalvarActionPerformed

