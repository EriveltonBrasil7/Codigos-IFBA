    private void botaoAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAtualizarActionPerformed
        Cargo cargo = new Cargo();
        cargo.setId(Integer.parseInt(textoCodigo.getText()));
        cargo.setNome(textoNome.getText());
        cargo.setRemuneracao(Double.parseDouble(textoRemuneracao.getText()));
        cargo.setDescricao(textoDescricao.getText());
        
       if(dao.atualizarCargo(cargo)){
           lista = dao.getAllCargos(ordenacao);
           preencherFrame();
           preencherTabela();
           JOptionPane.showMessageDialog(this, "Atualizado com sucesso!");
       }else{
           JOptionPane.showMessageDialog(this, "Erro ao atualizar!");
       }

    }//GEN-LAST:event_botaoAtualizarActionPerformed

    private void botaoExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoExcluirActionPerformed
        boolean status = dao.deletarCargo(Integer.parseInt(textoCodigo.getText()));

        if (status == true) {
            lista = dao.getAllCargos(ordenacao);
            posicao = 0;
            preencherFrame();
            preencherTabela();
            JOptionPane.showMessageDialog(this, "Excluído com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Não foi possível excluir!");
        }

    }//GEN-LAST:event_botaoExcluirActionPerformed

