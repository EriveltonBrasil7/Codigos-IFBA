    private void preencherFrame() {
        Cargo c = lista.get(posicao);
        textoCodigo.setText("" + c.getId());
        textoNome.setText(c.getNome());
        textoDescricao.setText(c.getDescricao());
        textoRemuneracao.setText("" + c.getRemuneracao());
        
        jTable1.setRowSelectionInterval(posicao, posicao);
        rolarScroll();
    }
    
    private void limparFrame(){
        botaoSalvar.setEnabled(true);
        botaoAtualizar.setEnabled(false);
        botaoExcluir.setEnabled(false);

        textoCodigo.setText("");
        textoNome.setText("");
        textoDescricao.setText("");
        textoRemuneracao.setText("");

        textoNome.requestFocus();
    }
    
    private void preencherTabela(){
        DefaultTableModel modeloTabela = (DefaultTableModel) jTable1.getModel(); 
        modeloTabela.setNumRows(0);
        
        Object[] linha;
        
        for(Cargo cargo : lista){
            linha = new Object[]{cargo.getId(), cargo.getNome(), 
                     cargo.getDescricao(), cargo.getRemuneracao()};
         
            modeloTabela.addRow(linha);
        }
        
        jTable1.setRowSelectionInterval(posicao, posicao);
        
    }

