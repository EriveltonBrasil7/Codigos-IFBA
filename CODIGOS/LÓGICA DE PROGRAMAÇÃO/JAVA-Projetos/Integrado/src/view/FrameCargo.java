    private void limparFormulario() {
        textoCodigo.setText("");
        textoAuxAlimentacao.setText("");
        textoDescricao.setText("");
        textoNome.setText("");
    }

    private void preencherFormulario() {
        Cargo cargo;
        cargo = listaCargos.get(indice);

        textoCodigo.setText("" + cargo.getCodigo());
        textoNome.setText(cargo.getNome());
        textoDescricao.setText(cargo.getDescricao());
        textoAuxAlimentacao.setText(Double.toString(cargo.getAuxAlimentacao()));
    }
    
    private void preencherTabela(){
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);
        
        Object[] linha;
        
        for (Cargo cargo : listaCargos) {
            linha = new Object[]{cargo.getCodigo(), cargo.getNome(), cargo.getDescricao(), cargo.getAuxAlimentacao()};
            modelo.addRow(linha);
        }
        
        
    }

