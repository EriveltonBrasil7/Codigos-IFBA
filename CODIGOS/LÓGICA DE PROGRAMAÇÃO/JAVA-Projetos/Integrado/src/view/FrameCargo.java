    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        Cargo cargo = new Cargo();
        cargo.setNome(textoNome.getText());
        cargo.setDescricao(textoDescricao.getText());
        cargo.setAuxAlimentacao(Double.parseDouble(textoAuxAlimentacao.getText()));  
        
        if(dao.salvarCargo(cargo)){
            listaCargos = dao.getCargos();
            indice = listaCargos.size()-1;
            preencherFormulario();
            preencherTabela();
            JOptionPane.showMessageDialog(this, "Salvo com sucesso!");
        }else{
            JOptionPane.showMessageDialog(this, "Erro ao salvar!");
        }
          
        botaoSalvar.setEnabled(false);
        botaoAtualizar.setEnabled(true);
        botaoRemover1.setEnabled(true);
    }//GEN-LAST:event_botaoSalvarActionPerformed

    private void botaoProximoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoProximoActionPerformed
        indice++;

        if (indice < listaCargos.size()) {
            preencherFormulario();
        } else {
            JOptionPane.showMessageDialog(this, "Último Registro!");
            indice--;
            System.out.println(indice);
        }


    }//GEN-LAST:event_botaoProximoActionPerformed

    private void botaoAnteriorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAnteriorActionPerformed
        indice--;

        if (indice >= 0) {
            preencherFormulario();
        } else {
            JOptionPane.showMessageDialog(this, "Primeiro Registro!");
            indice++;
            System.out.println(indice);
        }
    }//GEN-LAST:event_botaoAnteriorActionPerformed

