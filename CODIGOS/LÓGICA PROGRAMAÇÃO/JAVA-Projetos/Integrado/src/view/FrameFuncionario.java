    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        Funcionario funcionario = new Funcionario();
        funcionario.setNome(textoNome.getText());
        funcionario.setCpf(textoCpf.getText());
        funcionario.setSexo(comboSexo.getSelectedItem().toString());
        funcionario.setCargo(cargo); 
        
        
        if (dao.salvarFuncionario(funcionario)) {
            listaFuncionarios = dao.getFuncionarios(colunaOrderBy);
            indice = listaFuncionarios.size() - 1;
            preencherTabela();
            preencherFormulario();
            JOptionPane.showMessageDialog(this, "Salvo com sucesso!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar!");
        }

        botaoSalvar.setEnabled(false);
        botaoAtualizar.setEnabled(true);
        botaoRemover1.setEnabled(true);
    }//GEN-LAST:event_botaoSalvarActionPerformed

