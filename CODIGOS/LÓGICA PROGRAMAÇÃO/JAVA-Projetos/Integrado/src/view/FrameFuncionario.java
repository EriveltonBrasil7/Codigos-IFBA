    private void limparFormulario() {
        textoCodigo.setText("");
        textoCpf.setText("");
        textoNome.setText("");
        comboSexo.setSelectedIndex(0);
        textoCodigoCargo.setText("");
        textoNomeCargo.setText("");
        cargo = null;
    }

    private void preencherFormulario() {
        Funcionario funcionario;
        funcionario = listaFuncionarios.get(indice);

        textoCodigo.setText("" + funcionario.getCodigo());
        textoNome.setText(funcionario.getNome());
        textoCpf.setText(funcionario.getCpf());
        comboSexo.setSelectedItem(funcionario.getSexo()); 
        textoCodigoCargo.setText(funcionario.getCargo().getCodigo()+"");
        textoNomeCargo.setText(funcionario.getCargo().getNome());
        
        jTable1.setRowSelectionInterval(indice, indice);
    }

    private void preencherTabela() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);

        Object[] linha;

        for (Funcionario funcionario : listaFuncionarios) {
            linha = new Object[]{funcionario.getCodigo(), funcionario.getNome(), funcionario.getCpf(), funcionario.getSexo(), funcionario.getCargo().getNome()};
            modelo.addRow(linha);
        }

    }
    
    public void receberCargo(Cargo cargo){
        textoCodigoCargo.setText(cargo.getCodigo()+""); 
        textoNomeCargo.setText(cargo.getNome());
        this.cargo = cargo;
    }

