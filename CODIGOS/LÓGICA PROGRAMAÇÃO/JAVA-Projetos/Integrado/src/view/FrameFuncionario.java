    private void botaoRemover1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoRemover1ActionPerformed

        int opcao = JOptionPane.showConfirmDialog(this, "Tem certeza?", "Confirmação", JOptionPane.YES_NO_OPTION);

        if (opcao == 0) {
            int id = Integer.parseInt(textoCodigo.getText());
            boolean status = dao.deletarFuncionario(id);
            if (status) {
                listaFuncionarios = dao.getFuncionarios(colunaOrderBy);
                indice = 0;
                preencherTabela();
                preencherFormulario();
                JOptionPane.showMessageDialog(this, "Excluído com sucesso!");
            } else {
                JOptionPane.showMessageDialog(this, "Erro ao excluir!");
            }
        }


    }//GEN-LAST:event_botaoRemover1ActionPerformed

    private void botaoAtualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoAtualizarActionPerformed
        Funcionario funcionario = new Funcionario();
        funcionario.setCodigo(Integer.parseInt(textoCodigo.getText()));
        funcionario.setNome(textoNome.getText());
        funcionario.setCpf(textoCpf.getText());
        funcionario.setSexo(comboSexo.getSelectedItem().toString());
        if(cargo != null){
            funcionario.setCargo(cargo);
        }else{
            Funcionario funci = listaFuncionarios.get(indice);
            funcionario.setCargo(funci.getCargo());
        }
        
        if (dao.atualizarFuncionario(funcionario)) {
            listaFuncionarios = dao.getFuncionarios(colunaOrderBy);
            preencherTabela();
            preencherFormulario();

            JOptionPane.showMessageDialog(this, "Sucesso ao atualizar!");
        } else {
            JOptionPane.showMessageDialog(this, "Erro ao atualizar!");
        }

    }//GEN-LAST:event_botaoAtualizarActionPerformed

