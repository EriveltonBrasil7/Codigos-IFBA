 private void preencherTabela() {
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        modelo.setNumRows(0);

        Object[] linha;

        for (Cargo cargo : listaCargos) {
            linha = new Object[]{cargo.getCodigo(), cargo.getNome(), cargo.getDescricao(), cargo.getAuxAlimentacao(), cargo.getTipo(), cargo.getPreco()};
            modelo.addRow(linha);
        }

    }

