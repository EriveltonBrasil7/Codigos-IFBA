    private void botaoFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoFiltrarActionPerformed
        String colunaFiltro = comboFiltrar.getSelectedItem().toString();
        if (colunaFiltro.equals("Nome")) {
            colunaFiltro = "cargo_nome";
        } else {
            colunaFiltro = "cargo_id";
        }

        //        String aux = "";
        //        switch(colunaFiltro){
            //            case "Nome":
            //                aux = "nome";
            //                break;
            //            case "Código":
            //                aux = "codigo";
            //                break;
            //        }
        List<Cargo> listaTemp = dao.getFiltrarCargos(colunaOrderBy, colunaFiltro, textoFiltrar.getText());
        if (listaTemp.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ops! não achei nada!");
        } else {
            listaCargos = listaTemp;
            indice = 0;
            preencherTabela();
        }

    }//GEN-LAST:event_botaoFiltrarActionPerformed

