    private void preencherFrame() {
        Funcionario func = lista.get(posicao);
        textoCodigo.setText("" + func.getId());
        textoNome.setText(func.getNome());
        textoCpf.setText(func.getCpf());
        comboSexo.setSelectedItem(func.getSexo());

        if (func.getCargo() != null) {
            textoCodigoCargo.setText("" + func.getCargo().getId());
            textoNomeCargo.setText(func.getCargo().getNome());
        } else {
            textoCodigoCargo.setText("");
            textoNomeCargo.setText("");
        }

        jTable1.setRowSelectionInterval(posicao, posicao);

        int linhaSelecionada = jTable1.getSelectedRow();
        Rectangle retanguloVisivel = jTable1.getCellRect(linhaSelecionada, 0, true);
        jTable1.scrollRectToVisible(retanguloVisivel);

        botaoExcluir.setEnabled(true);
        botaoAtualizar.setEnabled(true);
        botaoSalvar.setEnabled(false);

        if (posicao == 0) {
            botaoAnterior.setEnabled(false);
            botaoPrimeiro.setEnabled(false);
            botaoProximo.setEnabled(true);
            botaoUltimo.setEnabled(true);
        } else if (posicao == lista.size() - 1) {
            botaoAnterior.setEnabled(true);
            botaoPrimeiro.setEnabled(true);
            botaoProximo.setEnabled(false);
            botaoUltimo.setEnabled(false);
        } else {
            botaoAnterior.setEnabled(true);
            botaoPrimeiro.setEnabled(true);
            botaoProximo.setEnabled(true);
            botaoUltimo.setEnabled(true);
        }
    }

    private void limparFrame() {
        botaoSalvar.setEnabled(true);
        botaoAtualizar.setEnabled(false);
        botaoExcluir.setEnabled(false);

        textoCodigo.setText("");
        textoNome.setText("");
        textoCpf.setText("");

        textoNome.requestFocus();

        textoCodigoCargo.setText("");
        textoNomeCargo.setText("");
        cargo = null;
    }

    private void preencherTabela() {
        DefaultTableModel modeloTabela = (DefaultTableModel) jTable1.getModel();
        modeloTabela.setNumRows(0);

        Object[] linha;

        for (Funcionario func : lista) {

            linha = new Object[]{func.getId(), func.getNome(), func.getCpf(), func.getSexo(), func.getCargo() == null ? "" : func.getCargo().getNome()};

            modeloTabela.addRow(linha);
        }

        jTable1.setRowSelectionInterval(posicao, posicao);

    }

