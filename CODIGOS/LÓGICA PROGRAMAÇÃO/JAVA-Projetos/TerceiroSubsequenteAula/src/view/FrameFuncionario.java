    private void botaoSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoSalvarActionPerformed
        Funcionario func = new Funcionario();
        func.setNome(textoNome.getText());
        func.setCpf(textoCpf.getText());
        func.setSexo(comboSexo.getSelectedItem().toString());
        func.setCargo(cargo);

        boolean status = dao.insertFuncionario(func);

        if (status == true) {
            JOptionPane.showMessageDialog(this, "Salvo com sucesso!");
            int opcao = JOptionPane.showConfirmDialog(this, "Deseja salvar outro Funcionário?", "Mensagem", JOptionPane.YES_NO_OPTION);

            if (opcao == 0) {
                limparFrame();
            } else {
                lista = dao.getAllFuncionarios(ordenacao);
                posicao = lista.size() - 1;
                preencherTabela();
                preencherFrame();
                botaoSalvar.setEnabled(false);
                botaoAtualizar.setEnabled(true);
                botaoExcluir.setEnabled(true);
            }

        } else {
            JOptionPane.showMessageDialog(this, "Erro ao salvar!");
        }


    }//GEN-LAST:event_botaoSalvarActionPerformed

