    private void botaoFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botaoFiltrarActionPerformed
        String campo = comboFiltrar.getSelectedItem().toString();
        if (campo.equals("Nome")) {
            campo = "funci_nome";
        } else {
            campo = "funci_id";
        }

        String valor = textoFiltrar.getText();

        List<Funcionario> listaTemporaria = dao.getFiltrarFuncionarios(campo, valor, ordenacao);

        if (listaTemporaria.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ops! não encontrei nada!");
        } else {
            lista = listaTemporaria;
            posicao = 0;
            preencherTabela();
            preencherFrame();
        }

        textoFiltrar.setText("");

    }//GEN-LAST:event_botaoFiltrarActionPerformed

